from threading import Thread

class IGThread(Thread):
  def __init__(self, texto, tempo):
    self.texto = texto
    self.tempo = tempo

    super().__init__()

  def run(self):
    sleep(self.tempo)
    print(self.texto)

t1 = IGThread('IGTI Thread 1', 5)
t1.start()


t2 = IGThread('IGTI Thread 2', 8)
t2.start()

